struct CustomNavView<Content: View>: View {
    
    let content: Content
    
    init(@ViewBuilder content: () -> Content) {
        self.content = content()
    }
    
    var body: some View {
        NavigationStack {
            CustomNavBarContainerView {
                content
            }
            //.navigationBarHidden(true)
        }
        //.navigationViewStyle(StackNavigationViewStyle())
    }
}

